
def reverse(items):
    #""" is for module documentation
    """reverse reverses the items in a list of items
	
	Parameters:
	
	     items: list of items (can be a string)
	
	"""
    reverseList = []	
    itemSize = len(items)	
    
    for i in range(itemSize, 0, -1):	    
        reverseList.append(items[i-1])
		
    return reverseList
	
	
